import { gql } from "@apollo/client";
export const FETCH_USED_ITEM = gql`
  query fetchUseditem($useditemId: ID!) {
    fetchUseditem(useditemId: $useditemId) {
      _id
      name
      remarks
      contents
      price
      createdAt
      pickedCount
      images
      tags
      useditemAddress {
        _id
        address
        addressDetail
      }
    }
  }
`;

